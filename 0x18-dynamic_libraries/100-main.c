#include <stdio.h>
/**
 * add -This is an function that return the added number
 * @num1: This is an argument that contain the first number
 * @num2: This is an argument that contain the second number
 *
 * Return: This function return the added number
 */
int add(int num1, int num2)
{
	return (num1 + num2);
}
